import UIKit

//MARK:- Font Constant
func appBoldFont(size : CGFloat) -> UIFont {
    return UIFont.boldSystemFont(ofSize: size)
}

func appFont(size : CGFloat) -> UIFont {
    return UIFont.systemFont(ofSize: size)
}




